// ── PRODUCT_DEBT auto-block ───────────────────────────────────────────────────────────────────
// Group hard fails by (routeId, budgetKey) → one debt row each, listing states + widths.
interface Grp {
  routeId: string;
  path: string;
  key: string;
  label: string;
  where: Set<string>;
  values: Set<string>;
  detail: Set<string>;
}
const groups = new Map<string, Grp>();
function grp(gk: string, r: ShotResult, key: string, label: string): Grp {
  const g = groups.get(gk) ?? {
    routeId: r.routeId,
    path: r.path,
    key,
    label,
    where: new Set(),
    values: new Set(),
    detail: new Set(),
  };
  groups.set(gk, g);
  return g;
}
for (const r of run.results) {
  if (r.status === "error") {
    const g = grp(
      `${r.routeId}:render-error`,
      r,
      "render-error",
      "Render error",
    );
    g.where.add(`${r.state}@${r.width}`);
    g.values.add((r.pageError ?? "error").slice(0, 60));
    continue;
  }
  for (const b of budgetsFor(r)) {
    if (!HARD_KEYS.has(b.key) || b.verdict !== "fail") continue;
    const g = grp(`${r.routeId}:${b.key}`, r, b.key, b.label);
    g.where.add(`${r.state}@${r.width}`);
    g.values.add(b.value);
    // For console/pageerror, attach a short sample so the founder knows what the error is.
    if (b.key === "console" && r.consoleSamples[0])
      g.detail.add(r.consoleSamples[0].slice(0, 70));
    if (b.key === "pageerror" && r.pageError)
      g.detail.add(r.pageError.slice(0, 70));
  }
}
// S1 = truly broken (dead page / uncaught error / bad status). Console/hydration warnings and
// layout budgets (overflow/CLS) are S2 — real, but the page still renders.
const severityFor = (key: string): string =>
  key === "render-error" || key === "pageerror" || key === "http" ? "S1" : "S2";

const debtRows: string[] = [];
let n = 1;
for (const g of groups.values()) {
  const where = [...g.where].sort().join(", ");
  const vals = [...g.values].join(" / ");
  const detail = g.detail.size ? ` — ${[...g.detail].join("; ")}` : "";
  const note = `QA-01 mechanical${detail}; screenshot in docs/qa/QA-01/`;
  debtRows.push(
    `| QA-${n} | \`${g.path}\` | ${g.label} miss (${vals}) at ${where} | ${severityFor(g.key)} | QA | OPEN | ${note} |`,
  );
  n++;
}

const block = [
  AUTO_START,
  `<!-- ${debtRows.length} row(s) from QA-01 run ${run.generatedAt} -->`,
  ...(debtRows.length
    ? debtRows
    : ["<!-- No hard budget misses this run. -->"]),
  AUTO_END,
].join("\n");

let debt = readFileSync(PRODUCT_DEBT, "utf8");
if (debt.includes(AUTO_START) && debt.includes(AUTO_END)) {
  debt = debt.replace(
    new RegExp(`${escapeRe(AUTO_START)}[\\s\\S]*?${escapeRe(AUTO_END)}`),
    block,
  );
} else {
  // Insert the auto-block just before the Changelog line (or append).
  const marker = "**Changelog:**";
  const idx = debt.indexOf(marker);
  const injected =
    "\n### QA-01 auto-filed (mechanical) rows\n\n" +
    "> Regenerated each QA-01 run. Broken states only (hard budget misses); advisory LCP/tap not filed.\n\n" +
    block +
    "\n\n";
  debt =
    idx === -1
      ? debt + injected
      : debt.slice(0, idx) + injected + debt.slice(idx);
}
writeFileSync(PRODUCT_DEBT, debt);

function escapeRe(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

