// QA-01 report generator. Reads docs/qa/QA-01/results.json (from the capture spec) and produces:
//   • docs/qa/QA-01/index.md — the mechanical pass/fail matrix vs DESIGN_DIRECTION v1.0 budgets;
//   • an auto-maintained block of PRODUCT_DEBT.md rows for BROKEN states (hard budget misses),
//     regenerated in place between markers so re-runs never duplicate and manual rows are untouched.
//
// Mechanical only — NO UX opinions. A "hard fail" is an objective, non-advisory budget miss
// (broken render / horizontal overflow / CLS). Dev-server LCP and tap-target checks are ADVISORY
// (dev builds are unoptimised; inline controls legitimately vary) → surfaced in the index, never
// auto-filed as debt.
//
//   npx tsx scripts/qa-report.ts
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";
import { THRESHOLDS, scoreBudgets, type BudgetLine } from "../e2e/qa/budgets";
import { ROUTES } from "../e2e/qa/routes";
import {
  RESULTS_JSON,
  OUT_DIR,
  type ShotResult,
  type RunManifest,
} from "../e2e/qa/results";

const routeMeta = new Map(ROUTES.map((r) => [r.id, r]));

const PRODUCT_DEBT = resolve(process.cwd(), "docs", "PRODUCT_DEBT.md");
const INDEX_MD = resolve(OUT_DIR, "index.md");
const AUTO_START =
  "<!-- QA-01:AUTO:START — regenerated by scripts/qa-report.ts, do not edit by hand -->";
const AUTO_END = "<!-- QA-01:AUTO:END -->";

// Budgets that are HARD (a miss = broken / debt) vs ADVISORY (reported, never auto-filed).
const HARD_KEYS = new Set(["http", "console", "pageerror", "overflow", "cls"]);

if (!existsSync(RESULTS_JSON)) {
  throw new Error(`No results at ${RESULTS_JSON}. Run the capture spec first.`);
}
const run: RunManifest = JSON.parse(readFileSync(RESULTS_JSON, "utf8"));

// Re-score budgets HERE from the raw recorded facts (metrics + final console/pageerror/http), rather
// than trusting the spec's stored r.budgets: console/pageerror events can arrive after the spec
// scored them, so the report is the single source of truth for verdicts.
function budgetsFor(r: ShotResult): BudgetLine[] {
  if (r.status !== "captured" || !r.metrics) return [];
  return scoreBudgets(r.metrics, THRESHOLDS, {
    httpOk: r.httpOk,
    httpStatus: r.httpStatus ?? 0,
    consoleErrors: r.consoleErrors,
    pageError: r.pageError,
  });
}

type Verdict = "PASS" | "ADVISORY" | "FAIL" | "SKIP" | "ERROR";
function verdictFor(r: ShotResult): Verdict {
  if (r.status === "skipped") return "SKIP";
  if (r.status === "error") return "ERROR";
  const budgets = budgetsFor(r);
  const hard = budgets.some(
    (b) => HARD_KEYS.has(b.key) && b.verdict === "fail",
  );
  if (hard) return "FAIL";
  const adv = budgets.some((b) => b.verdict === "advisory-fail");
  return adv ? "ADVISORY" : "PASS";
}
const VERDICT_BADGE: Record<Verdict, string> = {
  PASS: "✅ PASS",
  ADVISORY: "🟡 ADV",
  FAIL: "❌ FAIL",
  SKIP: "⏭️ SKIP",
  ERROR: "💥 ERR",
};

function cell(r: ShotResult): string {
  const v = verdictFor(r);
  if (r.status !== "captured") return VERDICT_BADGE[v];
  return `[${VERDICT_BADGE[v]}](${r.screenshot})`;
}

function budgetSummary(r: ShotResult): string {
  if (r.status !== "captured" || !r.metrics)
    return r.skipReason ?? r.pageError ?? "—";
  const m = r.metrics;
  const bits = [
    `ovf ${m.horizontalOverflowPx}px`,
    `CLS ${m.cls.toFixed(3)}`,
    `LCP ${m.lcpMs === null ? "n/a" : m.lcpMs + "ms"}`,
    `tap ${m.undersizedTapTargets}`,
  ];
  if (r.consoleErrors) bits.push(`**${r.consoleErrors} console err**`);
  if (r.pageError) bits.push(`**pageerror**`);
  if (r.redirected)
    bits.push(`↪ ${r.finalUrl ? new URL(r.finalUrl).pathname : "?"}`);
  return bits.join(" · ");
}

// ── Aggregate counts ───────────────────────────────────────────────────────────────────────────
const byVerdict: Record<Verdict, number> = {
  PASS: 0,
  ADVISORY: 0,
  FAIL: 0,
  SKIP: 0,
  ERROR: 0,
};
for (const r of run.results) byVerdict[verdictFor(r)]++;
const captured = run.results.filter((r) => r.status === "captured");

// Per-budget pass counts (captured shots only).
const budgetKeys = [
  "http",
  "console",
  "pageerror",
  "overflow",
  "cls",
  "lcp",
  "tap",
] as const;
const budgetLabel: Record<string, string> = {
  http: "HTTP status ok",
  console: "No console errors",
  pageerror: "No uncaught error",
  overflow: "No H-overflow (§6/§18)",
  cls: "CLS ≤ 0.1 (§10/§17)",
  lcp: "LCP < 2.5s (§6/§17, advisory)",
  tap: "Tap ≥44px (§18, advisory)",
};
function budgetStats(key: string): {
  pass: number;
  total: number;
  fails: ShotResult[];
} {
  let pass = 0;
  const fails: ShotResult[] = [];
  for (const r of captured) {
    const b = budgetsFor(r).find((x) => x.key === key);
    if (!b || b.verdict === "n/a") continue;
    if (b.verdict === "pass") pass++;
    else fails.push(r);
  }
  return { pass, total: pass + fails.length, fails };
}

